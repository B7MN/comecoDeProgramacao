# Exemplo de operações matemáticas
a = 3
b = 2
print(a + b) # Soma
print(a - b) # Subtração
print(a * b) # Multiplicação
print(a / b) # Divisão
print(a // b) # Divisão inteira
print(a % b) # Resto da divisão
print(a ** b) # Exponenciação

# Exemplo de operações com strings
a = "Hello"
b = "World"
print(a + b) # Concatenação
print(a * 3) # Repetição
print(a[0]) # Indexação
print(a[1:4]) # Fatiamento
print(len(a)) # Tamanho da string
print("Hello" in a) # Verifica se a string está contida na outra

# Exemplo de operações de comparação
a = 3
b = 2
print(a == b) # Igualdade
print(a != b) # Desigualdade
print(a > b) # Maior que
print(a < b) # Menor que
print(a >= b) # Maior ou igual que
print(a <= b) # Menor ou igual que

# Exemplo de operações lógicas
a = True
b = False
print(a and b) # E
print(a or b) # Ou
print(not a) # Não

# Exemplo de operações de atribuição
a = 3
a += 2 # a = a + 2
a -= 2 # a = a - 2
a *= 2 # a = a * 2
a /= 2 # a = a / 2
a %= 2 # a = a % 2
a **= 2 # a = a ** 2


